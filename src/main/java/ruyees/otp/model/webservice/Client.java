package ruyees.otp.model.webservice;

public class Client {

	/**
	 * AD和TDS的URL
	 */
	private String adUrl;

	/**
	 * 附件的URL
	 */
	private String attUrl;

	public String getAdUrl() {
		return adUrl;
	}

	public void setAdUrl(String adUrl) {
		this.adUrl = adUrl;
	}

	public String getAttUrl() {
		return attUrl;
	}

	public void setAttUrl(String attUrl) {
		this.attUrl = attUrl;
	}
}
